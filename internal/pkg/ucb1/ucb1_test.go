package ucb1

import (
	"testing"

	"github.com/stretchr/testify/require"
)

func TestCalculate(t *testing.T) {
	stat := New()
	stat.Add(1, 1, 10000000000000)
	stat.Add(2, 100, 10000000000000)
	stat.Add(3, 10000, 10000000000000)
	stat.Add(4, 100000, 10000000000000)
	stat.Add(5, 1000000, 10000000000000)
	stat.Add(6, 10000000, 10000000000000)
	stat.Add(7, 100000000, 10000000000000)
	stat.Add(8, 1000000000, 10000000000000)
	stat.Add(9, 10000000000, 10000000000000)
	stat.Add(10, 100000000000, 10000000000000)
	stat.Add(11, 100000000000, 10000000000000)
	stat.Add(12, 10000000000000, 10000000000000)
	stat.Add(13, 10000000000000, 100000000000000)
	stat.Add(14, 10000000000000, 1000000000000000)
	stat.Add(15, 10000000000000, 10000000000000000)
	stat.Add(16, 10000000000000, 100000000000000000)
	stat.Add(17, 10000000000000, 1000000000000000000)

	bannerID := stat.GetBest()
	require.Equal(t, uint(12), bannerID)
}
